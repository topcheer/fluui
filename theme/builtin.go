package theme

// Dracula returns the Dracula theme.
func Dracula() *Theme {
	return &Theme{
		Name: "Dracula",

		// Base
		Bg:     C(0x28, 0x2A, 0x36), // #282a36
		Fg:     C(0xF8, 0xF8, 0xF2), // #f8f8f2
		Accent: C(0xBD, 0x93, 0xF9), // #bd93f9 purple

		// Borders
		Border:       C(0x62, 0x72, 0xA4), // #6272a4
		BorderActive: C(0xBD, 0x93, 0xF9), // #bd93f9
		BorderMuted:  C(0x44, 0x47, 0x5A), // #44475a

		// Status
		Success: C(0x50, 0xFA, 0x7B), // #50fa7b green
		Error:   C(0xFF, 0x55, 0x55), // #ff5555 red
		Warning: C(0xF1, 0xFA, 0x8C), // #f1fa8c yellow
		Muted:   C(0x62, 0x72, 0xA4), // #6272a4

		// Code
		CodeBg: C(0x28, 0x2A, 0x36),
		CodeFg: C(0xFF, 0x79, 0xC6), // pink

		// Diff
		DiffAdd:  C(0x50, 0xFA, 0x7B), // green
		DiffDel:  C(0xFF, 0x55, 0x55), // red
		DiffMeta: C(0x62, 0x72, 0xA4), // gray-blue
		DiffHunk: C(0x8B, 0xE9, 0xFD), // cyan
		DiffFile: C(0xBD, 0x93, 0xF9), // purple

		// Blocks
		UserMsgBg:    NoColor(),
		UserMsgFg:    C(0x8B, 0xE9, 0xFD), // cyan
		ThinkingBg:   NoColor(),
		ThinkingFg:   C(0x62, 0x72, 0xA4), // gray-blue
		ToolCallBg:   NoColor(),
		ToolResultBg: NoColor(),
		ToolResultFg: C(0xBD, 0x93, 0xF9), // purple
		AssistantFg:  C(0xF8, 0xF8, 0xF2), // white

		// Input
		PromptFg:  C(0x8B, 0xE9, 0xFD), // cyan
		Separator: C(0x62, 0x72, 0xA4), // gray-blue

		// Overlay
		MaskBg: C(0, 0, 0), // black

		// Search
		SearchBarBg:   C(0x44, 0x47, 0x5A), // darker gray-blue
		SearchBarFg:   C(0xF8, 0xF8, 0xF2),
		SearchMatch:   C(0xBD, 0x93, 0xF9), // purple accent
		SearchNoMatch: C(0xFF, 0x55, 0x55), // red
	}
}

